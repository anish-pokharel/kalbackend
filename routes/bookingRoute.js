router.get('/admin/bus-seats/:busId/:journeyDate', verifyToken, isAdmin, async (req, res) => {
    try {
        const { busId, journeyDate } = req.params;
        
        // Get all seats for this bus
        const allSeats = await Seat.find({ busId, isActive: true }).sort('seatNumber');
        
        // Get all confirmed, paid bookings for this bus and date
        const bookings = await Booking.find({
            busId,
            travelDate: new Date(journeyDate),
            bookingStatus: { $in: ['confirmed', 'completed'] },
            paymentStatus: 'completed'
        }).populate('userId', 'firstName lastName phone');
        
        // Collect all booked seats with passenger details
        const bookedSeatsMap = new Map();
        bookings.forEach(booking => {
            booking.seats.forEach(seat => {
                bookedSeatsMap.set(seat.seatNumber, {
                    seatNumber: seat.seatNumber,
                    passengerName: seat.passengerName,
                    passengerPhone: seat.passengerPhone,
                    bookingId: booking.bookingId,
                    userId: booking.userId,
                    bookingStatus: booking.bookingStatus
                });
            });
        });
        
        // Prepare seats with status and passenger info
        const seatsWithStatus = allSeats.map(seat => {
            const bookedInfo = bookedSeatsMap.get(seat.seatNumber);
            return {
                ...seat.toObject(),
                status: bookedInfo ? 'booked' : 'available',
                passenger: bookedInfo || null
            };
        });
        
        // Get bus details
        const bus = await Bus.findById(busId).populate('routeId', 'origin destination departureTime arrivalTime');
        
        res.json({
            success: true,
            data: {
                bus: bus,
                journeyDate: journeyDate,
                seats: seatsWithStatus,
                totalSeats: allSeats.length,
                bookedSeats: bookedSeatsMap.size,
                availableSeats: allSeats.length - bookedSeatsMap.size
            }
        });
    } catch (error) {
        console.error('Error fetching bus seats:', error);
        res.status(500).json({
            success: false,
            message: 'Error fetching bus seats',
            error: error.message
        });
    }
});
