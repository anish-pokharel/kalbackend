router.get('/seats/available/:busId/:travelDate', async (req, res) => {
    try {
        const { busId, travelDate } = req.params;
        
        console.log(`🔍 Checking available seats for bus ${busId} on date ${travelDate}`);
        
        // First, get the bus details
        const bus = await Bus.findById(busId);
        if (!bus) {
            return res.status(404).json({
                success: false,
                message: 'Bus not found'
            });
        }
        
        console.log(`📌 Bus found: ${bus.busNumber} - ${bus.busName}`);
        
        // Parse the date correctly for full day
        const startDate = new Date(travelDate);
        startDate.setHours(0, 0, 0, 0);
        const endDate = new Date(travelDate);
        endDate.setHours(23, 59, 59, 999);
        
        // Find all bookings for this bus on this date
        // Include both 'confirmed' and 'completed' status, and both 'paid' and 'pending' payment status
        const bookings = await Booking.find({
            busId: busId,
            journeyDate: { $gte: startDate, $lte: endDate },
            bookingStatus: { $in: ['confirmed', 'completed'] },
            paymentStatus: { $in: ['paid', 'pending'] }  // Include pending payments too
        }).select('seats bookingId');
        
        console.log(`📊 Found ${bookings.length} bookings for bus ${busId} on ${travelDate}`);
        
        // Collect all booked seats with details
        const bookedSeats = [];
        
        bookings.forEach(booking => {
            console.log(`Processing booking ${booking.bookingId}`);
            
            // Check seats array
            if (booking.seats && booking.seats.length > 0) {
                booking.seats.forEach(seat => {
                    if (seat && seat.seatNumber) {
                        bookedSeats.push({
                            seatNumber: seat.seatNumber,
                            passengerName: seat.passengerName || 'Booked',
                            passengerPhone: seat.passengerPhone || 'N/A',
                            bookingId: booking.bookingId,
                            status: 'booked'
                        });
                        console.log(`📌 Booked seat: ${seat.seatNumber}`);
                    }
                });
            }
        });
        
        // Remove duplicates
        const uniqueBookedSeats = [];
        const seenSeats = new Set();
        for (const seat of bookedSeats) {
            if (!seenSeats.has(seat.seatNumber)) {
                seenSeats.add(seat.seatNumber);
                uniqueBookedSeats.push(seat);
            }
        }
        
        console.log(`✅ Total booked seats: ${uniqueBookedSeats.length}`);
        console.log(`📋 Booked seats: ${uniqueBookedSeats.map(s => s.seatNumber).join(', ')}`);
        
        res.json({
            success: true,
            data: uniqueBookedSeats,
            count: uniqueBookedSeats.length,
            busNumber: bus.busNumber,
            travelDate: travelDate
        });
        
    } catch (error) {
        console.error('❌ Error fetching available seats:', error);
        res.status(500).json({
            success: false,
            message: 'Error fetching seats',
            error: error.message
        });
    }
});
