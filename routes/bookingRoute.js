// ============= CREATE BOOKING =============
router.post('/', verifyToken, async (req, res) => {
    const session = await mongoose.startSession();
    session.startTransaction();
    
    try {
        const {
            busId,
            seats,
            totalAmount,
            taxAmount,
            journeyDate,
            paymentMethod,
            paymentDetails,
            boardingPoint
        } = req.body;
        
        // Validate required fields
        if (!busId || !seats || !seats.length || !totalAmount || !paymentMethod || !journeyDate) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: 'Missing required fields'
            });
        }
        
        // Get bus details
        const bus = await Bus.findById(busId).session(session);
        if (!bus) {
            await session.abortTransaction();
            session.endSession();
            return res.status(404).json({
                success: false,
                message: 'Bus not found'
            });
        }
        
        // Get route details
        const route = await Route.findById(bus.routeId).session(session);
        if (!route) {
            await session.abortTransaction();
            session.endSession();
            return res.status(404).json({
                success: false,
                message: 'Route not found for this bus'
            });
        }
        
        // Check if seats are still available
        const bookedSeats = await Booking.find({
            busId,
            travelDate: new Date(journeyDate),
            bookingStatus: 'confirmed',
            paymentStatus: 'completed',
            $or: [
                { 'selectedSeats.seatNumber': { $in: seats.map(s => s.seatNumber) } },
                { 'seats.seatNumber': { $in: seats.map(s => s.seatNumber) } }
            ]
        }).session(session);
        
        if (bookedSeats.length > 0) {
            await session.abortTransaction();
            session.endSession();
            return res.status(400).json({
                success: false,
                message: 'Some seats are no longer available'
            });
        }
        
        // Create booking (bookingId will be auto-generated by pre-save hook)
        const booking = new Booking({
            userId: req.user.userId,
            busId: busId,
            routeId: bus.routeId,
            seats: seats.map(s => ({
                seatNumber: s.seatNumber,
                passengerName: s.passengerName,
                passengerAge: Number(s.passengerAge),
                passengerGender: s.passengerGender,
                passengerPhone: s.passengerPhone,
                passengerEmail: s.passengerEmail || ''
            })),
            selectedSeats: seats.map(s => ({
                seatNumber: s.seatNumber,
                seatType: 'passenger',
                price: totalAmount / seats.length
            })),
            passengerDetails: seats.map(s => ({
                name: s.passengerName,
                age: Number(s.passengerAge),
                gender: s.passengerGender,
                seatNumber: s.seatNumber,
                phone: s.passengerPhone,
                email: s.passengerEmail || ''
            })),
            totalAmount: Number(totalAmount),
            taxAmount: Number(taxAmount) || 0,
            paymentMethod: paymentMethod,
            paymentStatus: paymentMethod === 'cash' ? 'pending' : 'completed',
            paymentDetails: paymentDetails || {},
            boardingPoint: boardingPoint || '',
            bookingStatus: 'confirmed',
            travelDate: new Date(journeyDate),
            journeyDate: new Date(journeyDate),
            contactNumber: seats[0]?.passengerPhone || '',
            email: seats[0]?.passengerEmail || ''
        });
        
        await booking.save({ session });
        
        // Update seat status if Seat model exists
        if (Seat) {
            await Seat.updateMany(
                {
                    busId: busId,
                    seatNumber: { $in: seats.map(s => s.seatNumber) }
                },
                { status: 'booked', bookedBy: req.user.userId, bookedAt: new Date() },
                { session }
            );
        }
        
        await session.commitTransaction();
        session.endSession();
        
        // Populate and return the booking
        const populatedBooking = await Booking.findById(booking._id)
            .populate('busId', 'busName busNumber busType operator')
            .populate('routeId', 'origin destination departureTime arrivalTime duration distance');
        
        const responseData = {
            _id: populatedBooking._id,
            bookingId: populatedBooking.bookingId,
            busDetails: {
                busName: populatedBooking.busId?.busName || 'N/A',
                busNumber: populatedBooking.busId?.busNumber || 'N/A',
                busType: populatedBooking.busId?.busType || 'N/A',
                operator: populatedBooking.busId?.operator || 'N/A'
            },
            routeDetails: {
                origin: populatedBooking.routeId?.origin || 'N/A',
                destination: populatedBooking.routeId?.destination || 'N/A',
                departureTime: populatedBooking.routeId?.departureTime || 'N/A',
                arrivalTime: populatedBooking.routeId?.arrivalTime || 'N/A',
                duration: populatedBooking.routeId?.duration || 'N/A',
                distance: populatedBooking.routeId?.distance || 0
            },
            seats: populatedBooking.seats,
            passengers: populatedBooking.passengerDetails,
            totalAmount: populatedBooking.totalAmount,
            taxAmount: populatedBooking.taxAmount,
            journeyDate: populatedBooking.journeyDate,
            paymentMethod: populatedBooking.paymentMethod,
            paymentStatus: populatedBooking.paymentStatus,
            bookingStatus: populatedBooking.bookingStatus,
            createdAt: populatedBooking.createdAt,
            boardingPoint: populatedBooking.boardingPoint
        };
        
        res.status(201).json({
            success: true,
            message: 'Booking created successfully',
            data: responseData
        });
        
    } catch (error) {
        await session.abortTransaction();
        session.endSession();
        console.error('Error creating booking:', error);
        res.status(500).json({
            success: false,
            message: 'Error creating booking',
            error: error.message
        });
    }
});

